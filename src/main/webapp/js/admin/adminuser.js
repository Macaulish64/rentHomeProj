
function stringUserType(data)
{
    if (data==0) return '普通用户';
    if (data==1) return '管理员';
}

function stringUserLevel(data)
{
    if (data==0) return '一般';
    if (data==1) return '重要';
    if (data==2) return '钻石级';
}

function stringcardType(data)
{
    if (data==0) return '身份证';
    if (data==1) return '护照';
}

